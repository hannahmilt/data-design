		<h1>Interaction Flow</h1>
		<!-- need to have what the application does responding to what cheryl does(ex: cheryl pushes button, application opens up)-->
			<p>
				<ul>
					<li>Cheryl opens the Instagram application on her mobile phone.</li>
					<li> The application will show her home page. This displays her post as well post from the people she has followed.</li>
					<li>She clicks the new post icon in tool bar. This is located at bottom of page in the middle. Looks like a plus sign in the middle of a square</li>
					<li>Application will bring up libray of photos that are stored on phone.</li>
					<li>On the bottom in middle of screen it says Photo. Cheryl taps on word.</li>
					<li>The application will bring up a square veiw of the phone's camera, With a white circle button underneath</li>
					<li> She sets up her resin figure and brings it within the cameras frame. She presses the white button, which captures a picture of a resign figure she made.</li>
					<li>A series of squares will show up where the camera button once was. These are the filter options which can alter the picture look if you click on them. The boxes have the name of the filter above them.</li>
					<li>Decides to use no filter for her photo. Selecting the Normal box and clicking the next text in the top right of screen.</li>
					<li>The screen changes to a small picture box in the top left with a caption box next to it where you can type. There is also various selections available under that such as tagging (which links a person to your photo), location adding (adds place where you took photo), as well as shares your new post with facebook, Twitter, and Tumber applications; if you have them.</li>
					<li>Cheryl Adds a discription of figure in caption box as well as link to Etsy page. She also types in her city and state in the location box.</li>
					<li> Now the writing is displayed in caption box. Where it said location it now says her city and state</li>
					<li> She presses the Share text in the top right corner. </li>
					<li> The home page is now displayed with the new post of her resin figure at the top.</li>
					<li>Next day checks on activity to see who liked and what comments she recieved on post. She does this by clicking on the heart icon on bottom of screen.</li>
					<li>Application will show a screen of Activity such as recent comments and likes in list form with the picture of the person who did the action next to it.</li>
					<li>Monitors if her Etsy sales go up over the next month after posting.</li>
				</ul>
